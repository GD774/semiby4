CREATE TABLE USER_T (
   USER_NO      NUMBER   NOT NULL,
   USER_ID      VARCHAR2(128 BYTE)   NOT NULL UNIQUE,
   ROLE          NUMBER             DEFAULT 0 NOT NULL,
   PW            VARCHAR2(64 BYTE)  NOT NULL,
   EMAIL        VARCHAR2(100 BYTE)   NULL,
   NAME         VARCHAR2(100 BYTE) NULL,
   GENDER       VARCHAR2(5 BYTE)   NULL,
   MOBILE       VARCHAR2(20 BYTE)  NULL,
   PW_MODIFY_DT DATE               NULL,
   SIGNUP_DT    DATE               NULL,
   DELETED_DT   DATE               NULL,
    CONSTRAINT PK_USER_NO PRIMARY KEY(USER_NO)
);

CREATE TABLE BOARD_T (
   CATE_NO     NUMBER               NOT NULL,
   BOARD_NO    NUMBER               NOT NULL,
   USER_NO       NUMBER               NOT NULL,
   TITLE       VARCHAR2(1000 BYTE)   NOT NULL,
   CONTENTS    CLOB               NULL,
   HIT           NUMBER               DEFAULT 0,
   CREATE_DT   TIMESTAMP           NULL,
   MODIFY_DT   TIMESTAMP           NULL,
    CONSTRAINT PK_BOARD_NO      PRIMARY KEY (BOARD_NO),
    CONSTRAINT FK_BOARD_USER_NO FOREIGN KEY(USER_NO)
    REFERENCES USER_T(USER_NO) ON DELETE CASCADE
);

CREATE TABLE ATTACH_T (
   ATTACH_NO         NUMBER            NOT NULL,
   UPLOAD_PATH         VARCHAR2(500 BYTE) NULL,
   FILESYSTEM_NAME     VARCHAR2(500 BYTE) NULL,
   ORIGINAL_FILENAME VARCHAR2(500 BYTE) NULL,
   DOWNLOAD_COUNT     NUMBER            NULL,
   HAS_THUMBNAIL     NUMBER            NULL,
   BOARD_NO         NUMBER            NOT NULL,
    CONSTRAINT PK_ATTACH_NO PRIMARY KEY(ATTACH_NO),
    CONSTRAINT FK_ATTACH_BOARD_NO FOREIGN KEY(BOARD_NO)
    REFERENCES BOARD_T(BOARD_NO) ON DELETE CASCADE
);

CREATE TABLE COMMENT_T (
   COMMENT_NO NUMBER               NOT NULL,
   USER_NO    NUMBER               NOT NULL,
   CONTENTS   VARCHAR2(2000 BYTE)   NOT NULL,
   CREATE_DT  TIMESTAMP            NULL,
   STATE      NUMBER               NULL,
   DEPTH      NUMBER               NULL,
   GROUP_NO   NUMBER               NULL,
   BOARD_NO   NUMBER               NOT NULL,
    CONSTRAINT PK_COMMENT_NO PRIMARY KEY (COMMENT_NO),
    CONSTRAINT FK_COMMENT_USER_NO FOREIGN KEY (USER_NO)
    REFERENCES USER_T(USER_NO) ON DELETE CASCADE
);

CREATE TABLE REPORT_T (
   REPORT_NO 		  NUMBER		NOT NULL,
   REPORTER_NO		  NUMBER		NOT NULL,
   USER_NO			  NUMBER		NULL,
   BOARD_NO			  NUMBER		NULL,
   TITLE			  VARCHAR2(2000 BYTE)	NULL,
   CONTENTS			  VARCHAR2(2000 BYTE)	NULL,
   CREATE_DT		  DATE			NOT NULL,
   CONSTRAINT PK_REPORT_NO PRIMARY KEY (REPORT_NO),
   CONSTRAINT FK_REPORT_REPORTER_NO FOREIGN KEY (REPORTER_NO)
   REFERENCES USER_T(USER_NO) ON DELETE CASCADE,
   CONSTRAINT FK_REPORT_USER_NO FOREIGN KEY (USER_NO)
   REFERENCES USER_T(USER_NO) ON DELETE CASCADE
);

CREATE TABLE PENALTY_T (
   PENALTY_NO		   NUMBER		NOT NULL,
   USER_NO			   NUMBER		NOT NULL,
   NOTE				   VARCHAR2(2000 BYTE)		NULL,
   ACTIONED_DT		   DATE			NULL,
   CONSTRAINT PK_PENALTY_NO PRIMARY KEY (PENALTY_NO),
   CONSTRAINT FK_PENALTY_USER_NO FOREIGN KEY (USER_NO)
   REFERENCES USER_T(USER_NO) ON DELETE CASCADE
);

CREATE TABLE SCHEDULE_T (
   SCHEDULE_NO			NUMBER				NOT NULL,
   USER_NO				NUMBER				NOT NULL,
   TITLE				VARCHAR2(50 BYTE)	NOT NULL,
   CONTENTS				VARCHAR2(1000 BYTE)	NULL,
   REMINDER				NUMBER		  		NULL,
   START_DATE			DATE				NOT NULL,
   END_DATE				DATE				NULL,
    CONSTRAINT PK_SCHEDULE_T PRIMARY KEY (SCHEDULE_NO),
	CONSTRAINT FK_SCHEDULE_USER_NO FOREIGN KEY (USER_NO)
	REFERENCES USER_T(USER_NO) ON DELETE CASCADE
);

